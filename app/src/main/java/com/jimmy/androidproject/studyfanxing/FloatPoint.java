public class FloatPoint {
    private Float x;       // 表示X坐标
    private Float y;       // 表示Y坐标

    public void setX(Float x) {
        this.x = x;
    }

    public void setY(Float y) {
        this.y = y;
    }

    public Float getX() {
        return this.x;
    }

    public Float getY() {
        return this.y;
    }

}
